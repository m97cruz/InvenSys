    public void llenarCampos() throws SQLException{     
        
        txtProdNombre.setText(aProd.getNombre());
        lblCodigo.setText("Codigo: "+aProd.getCodigo());
        txtPreCompra.setText(String.valueOf(aProd.getPreCompra()));
        txtPreVenta.setText(String.valueOf(aProd.getPreVenta()));
        spinGanancia.setValue(aProd.getPorceGan());
        spinPack.setValue(aProd.getCantPack());
        txtPrePack.setText(String.valueOf(aProd.getPrePack()));
        spinLocal.setValue(aProd.getLocalCant());
        spinBodega.setValue(aProd.getBodegaCant());
        spinRepos.setValue(aProd.getRepo());
        
        //Para los Label de los Proveedores
        
        if(!aProd.getProv1().equals("")){
             lblProv1.setText(">>> "+aProd.getProv1());
             p1=true;
         }else{
             lblProv1.setText(" ");
             p1=false;
         }
        
        if(!aProd.getProv2().equals("")){
             lblProv2.setText(">>> "+aProd.getProv2());
             p2=true;
         }else{
             lblProv2.setText(" ");
             p2=false;
         }
        
        if(!aProd.getProv3().equals("")){
             lblProv3.setText(">>> "+aProd.getProv3());
             p3=true;
             
         }else{
             lblProv3.setText(" ");
             p3=false;
         }
        
        if(!aProd.getProv4().equals("")){
             lblProv4.setText(">>> "+aProd.getProv4());
             p4=true;
         }else{
             lblProv4.setText(" ");
             p4=false;
         }
        if(!aProd.getMarca().equals("")){
            marka=true;
            lblMarca.setText(aProd.getMarca());
            btnMarca.setText("Quitar");
        }else{
            marka=false;
            lblMarca.setText("");
            btnMarca.setText("Seleccionar");
        }
    }
